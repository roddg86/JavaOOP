package Seminars.Java.sem1;

public class Animal {
    // поля класса
    public String name;
    private String color;
    private int pawsCount;

    // конструктор класса
    public Animal(String name, String color, int pawsCount) {
        this.name = name;
        this.color = color;
        this.pawsCount = pawsCount;
    }

}
